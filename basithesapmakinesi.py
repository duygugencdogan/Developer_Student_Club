#fonksiyonları oluşturmak
def toplama(param1, param2):
    sonuc = param1 + param2
    return sonuc
def cikarma(param1, param2):
    sonuc = param1 - param2
    return sonuc
def carpma(param1, param2):
    sonuc =param1 * param2
    return sonuc
def bolme(param1, param2):
    sonuc = param1/param2
    return sonuc
def usalma(param1, param2):
    sonuc = param1 ** param2
    return sonuc

#fonksiyonları çağırmak
print(toplama(1, 2))
